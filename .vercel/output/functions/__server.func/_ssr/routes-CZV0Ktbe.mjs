function getPropertyNumericPrice(priceStr) {
	const clean = priceStr.replace(/[₹,]/g, "").trim();
	if (clean.includes("Cr")) return {
		amount: parseFloat(clean.replace("Cr.", "").trim()) * 1e7,
		isMonthly: false
	};
	if (clean.includes("Month")) return {
		amount: parseInt(clean.replace("/ Month", "").trim(), 10),
		isMonthly: true
	};
	return {
		amount: 0,
		isMonthly: false
	};
}
